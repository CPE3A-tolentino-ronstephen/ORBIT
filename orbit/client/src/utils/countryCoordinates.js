export function enrichWithCoords(record) {
  const coords = COUNTRY_COORDS[record.countryCode];
  if (!coords) return record;
  return { ...record, lat: coords.lat, lng: coords.lng, continent: coords.continent };
}

export function enrichCountriesWithCoords(countries) {
  return countries
    .map(enrichWithCoords)
    .filter(c => c.lat != null && c.lng != null);
}